package com.cucumber007.pillbox.objects.gym;

public class ProducerData {

    int id;
    String avatarUrl;
    String fullName;
    String description;
    String experience;
    float rating;

    public int getId() {
        return id;
    }

    public String getAvatarUrl() {
        return avatarUrl;
    }

    public String getFullName() {
        return fullName;
    }

    public String getDescription() {
        return description;
    }

    public String getExperience() {
        return experience;
    }

    public float getRating() {
        return rating;
    }
}
